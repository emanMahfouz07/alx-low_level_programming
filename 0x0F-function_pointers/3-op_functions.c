#include "3-calc.h"

/**
 * op_add - return the sum of two numbers
 * @a:first number
 * @b:second number
 *
 * Return:add of two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the subtraction of two numbers
 * @a:first number
 * @b:second number
 *
 * Return:subtract of two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the multipliction of two numbers
 * @a:first number
 * @b:second number
 *
 * Return:multiplication of two numbers
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the division of two numbers
 * @a:first number
 * @b:second number
 *
 * Return:division of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the reminder of two numbers
 * @a:first number
 * @b:second number
 *
 * Return:reminder of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
